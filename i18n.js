const I18n = {
  lang: 'da',

  init() {
    const saved = localStorage.getItem('portfolio-lang');
    const browserLang = navigator.language?.startsWith('da') ? 'da' : 'en';
    this.lang = saved || browserLang;
    this.apply();
    this.bindToggle();
  },

  t(key) {
    return translations[this.lang]?.[key] ?? translations.en[key] ?? key;
  },

  apply() {
    document.documentElement.lang = this.lang;
    document.querySelectorAll('[data-i18n]').forEach((el) => {
      const key = el.dataset.i18n;
      const value = this.t(key);
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = value;
      } else {
        el.textContent = value;
      }
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
      el.placeholder = this.t(el.dataset.i18nPlaceholder);
    });
    document.querySelectorAll('[data-i18n-aria]').forEach((el) => {
      el.setAttribute('aria-label', this.t(el.dataset.i18nAria));
    });
    document.querySelectorAll('[data-i18n-alt]').forEach((el) => {
      el.setAttribute('alt', this.t(el.dataset.i18nAlt));
    });
    document.querySelectorAll('[data-skills-list]').forEach((ul) => {
      const items = this.t(ul.dataset.skillsList).split('·').map((s) => s.trim()).filter(Boolean);
      if (ul.classList.contains('skill-tags--coverflow')) {
        ul.innerHTML = `<li><span class="skill-card-items">${items.join(' · ')}</span></li>`;
        return;
      }
      ul.innerHTML = items.map((item) => `<li><span class="skill-tag"># ${item}</span></li>`).join('');
    });
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc) metaDesc.content = this.t('meta.description');
    const langBtn = document.getElementById('lang-toggle');
    if (langBtn) {
      langBtn.textContent = this.lang === 'da' ? 'EN' : 'DA';
      langBtn.setAttribute('aria-label', this.t('lang.switch'));
    }
    document.dispatchEvent(new CustomEvent('langchange', { detail: { lang: this.lang } }));
  },

  toggle() {
    this.lang = this.lang === 'da' ? 'en' : 'da';
    localStorage.setItem('portfolio-lang', this.lang);
    this.apply();
  },

  bindToggle() {
    const btn = document.getElementById('lang-toggle');
    if (btn) btn.addEventListener('click', () => this.toggle());
  },
};
